using DES_Cipher_Logic;
using Microsoft.VisualStudio.TestTools.UnitTesting;

namespace DES_Cipher_Tests
{
    [TestClass]
    public class DES_Tests
    {
        [TestMethod]
        public void DES_Encryption_Works()
        {
            DES_Cipher _des = new DES_Cipher();

            var result = _des.Encrypt("0100000101001100010000010100110101000001010010110100111101010100", "0011101100111000100110000011011100010101001000001111011101011110");

            Assert.AreEqual("0111000100001110111101100111010000100000000001101010000111010001", result);
            result = _des.Encrypt("0100100001010101010010100100001101001001010101110100010001010000", "0011101100111000100110000011011100010101001000001111011101011110");
            Assert.AreEqual("1000100000001100101000010111001000001000011111101000111101111111", result);
        }
        [TestMethod]
        public void DES_Decryption_Works()
        {
            DES_Cipher _des = new DES_Cipher();

            var result = _des.Decrypt("0111000100001110111101100111010000100000000001101010000111010001", "0011101100111000100110000011011100010101001000001111011101011110");

            Assert.AreEqual("0100000101001100010000010100110101000001010010110100111101010100", result);
            result = _des.Decrypt("1000100000001100101000010111001000001000011111101000111101111111", "0011101100111000100110000011011100010101001000001111011101011110");
        }
    }
}
